CREATE POLICY "Admin can update all feedbacks" ON public.feedbacks FOR UPDATE USING (auth.uid() = 'f2fb4c9c-169b-447d-b8a6-dce72c4ed5ac');

-- Policies: Feedback Replies
DROP POLICY IF EXISTS "Users can view replies to their own feedback" ON public.feedback_replies;
CREATE POLICY "Users can view replies to their own feedback" 
ON public.feedback_replies FOR SELECT 
USING (
  EXISTS (
    SELECT 1 FROM public.feedbacks 
    WHERE feedbacks.id = feedback_replies.feedback_id 
    AND feedbacks.user_id = auth.uid()
  )
);

DROP POLICY IF EXISTS "Users can insert replies to their own feedback" ON public.feedback_replies;
CREATE POLICY "Users can insert replies to their own feedback" 
ON public.feedback_replies FOR INSERT 
WITH CHECK (
  EXISTS (
    SELECT 1 FROM public.feedbacks 
    WHERE feedbacks.id = feedback_replies.feedback_id 
    AND feedbacks.user_id = auth.uid()
  )
);

DROP POLICY IF EXISTS "Admin can view all replies" ON public.feedback_replies;
CREATE POLICY "Admin can view all replies" ON public.feedback_replies FOR SELECT USING (auth.uid() = 'f2fb4c9c-169b-447d-b8a6-dce72c4ed5ac');

DROP POLICY IF EXISTS "Admin can insert replies" ON public.feedback_replies;
CREATE POLICY "Admin can insert replies" ON public.feedback_replies FOR INSERT WITH CHECK (auth.uid() = 'f2fb4c9c-169b-447d-b8a6-dce72c4ed5ac');

-- Trigger for updated_at
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

DROP TRIGGER IF EXISTS update_user_subscriptions_updated_at ON public.user_subscriptions;
CREATE TRIGGER update_user_subscriptions_updated_at BEFORE UPDATE ON public.user_subscriptions FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();

DROP TRIGGER IF EXISTS update_feedbacks_updated_at ON public.feedbacks;
CREATE TRIGGER update_feedbacks_updated_at BEFORE UPDATE ON public.feedbacks FOR EACH ROW EXECUTE PROCEDURE update_updated_at_column();
